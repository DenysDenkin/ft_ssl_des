#include "libft.h"

int		ft_countmant(double n)
{
	int			i;
	double		temp;

	i = 0;
	if (n >= 0)
		temp = n;
	else
		temp = -n;
	while ((temp /= 10) >= 1)
		i++;
	return (i + 1);
}
