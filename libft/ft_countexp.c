#include "libft.h"

int		ft_countexp(double num)
{
	int		exp;

	exp = 0;
	if (num > 1 || num < -1)
		while ((num >= 10 || num <= -10))
		{
			num /= 10;
			exp++;
		}
	else
		while (num <= 1 && num >= -1 && num != 0)
		{
			num *= 10;
			exp--;
		}
	return (exp);
}
